class Main {
    public static void main(String[] args) {
        int[] nums={25,75,-1,16,90,0,79,-2,6};
        int count=0;
        for(int i=1;i<nums.length;i++){
            if(nums[i]%2==0){
                count++;
    
        }
        
    }
    System.out.println("no of even numbers in an array is:"+count);
 }
}